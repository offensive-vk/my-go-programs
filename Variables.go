package main

import "fmt"

func main() {
    // Variables with explicit types
    var age int = 25
    var name string = "Alice"

    // Variables with type inference
    isProgrammer := true
    height := 5.8

    // Constants
    const pi = 3.14159

    // Print values
    fmt.Println("Name:", name)
    fmt.Println("Age:", age)
    fmt.Println("Is Programmer:", isProgrammer)
    fmt.Println("Height:", height)
    fmt.Println("Value of Pi:", pi)
}
